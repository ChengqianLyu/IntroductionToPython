import rosegraphics as rg
window=rg.TurtleWindow()
matttt=rg.SimpleTurtle('turtle')
matttt.pen=rg.Pen('red',25)
matttt.speed=5
matttt.forward(120)
matttt.right(120)
matttt.forward(240)
matttt.right(120)
matttt.forward(240)
matttt.right(120)
matttt.forward(120)
mattt=rg.SimpleTurtle('turtle')
mattt.pen=rg.Pen('red',25)
mattt.speed=5
mattt.backward(20)
mattt.right(120)
mattt.forward(180)
mattt.left(120)
mattt.forward(240)
mattt.left(120)
mattt.forward(240)
mattt.left(120)
mattt.forward(60)

window.close_on_mouse_click()
